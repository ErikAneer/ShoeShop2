public class ShoeColour {

    private int id;
    private String colour;

    public ShoeColour(int id, String colour) {
        this.id = id;
        this.colour = colour;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public int getId() {
        return id;
    }

    public String getColour() {
        return colour;
    }
}
